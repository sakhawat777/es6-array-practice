// unshift() This method adds one or more elements to the beginning of an array and returns the new length of the array.

const array = ['Biplob', 'Sakhawat', 'Tina', 'Bina'];
console.log(array.unshift('Mina', 'Raja')); // 6
console.log(array);
// [ 'Mina', 'Raja', 'Biplob', 'Sakhawat', 'Tina', 'Bina' ]
